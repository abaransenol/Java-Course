    public void populateTable(){
        model = (DefaultTableModel)tblCities.getModel();
        model.setRowCount(0);
        
        DatabaseManager databaseManager = new DatabaseManager();
        ArrayList<City> cities = databaseManager.selectData();
        for(City city: cities){
            Object[] rows = {
                city.getId(),
                city.getName(),
                city.getPopulation(),
                city.getAltitude()
            };
            
            model.addRow(rows);
        }
    }

