    private void txtSearchKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtSearchKeyReleased
        TableRowSorter<DefaultTableModel> tableRowSorter = new TableRowSorter<DefaultTableModel>(model);
        tblCities.setRowSorter(tableRowSorter);
        
        String txt = txtSearch.getText();
        tableRowSorter.setRowFilter(RowFilter.regexFilter(txt));
    }//GEN-LAST:event_txtSearchKeyReleased

    private void txtAddToTableActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txtAddToTableActionPerformed
        DatabaseManager databaseManager = new DatabaseManager();
        City city = new City(
                txtCityName.getText(),
                Integer.valueOf(txtPopulation.getText()),
                Integer.valueOf(txtAltitude.getText())
        );
        
        databaseManager.insertData(city);
        lblAddedMessage.setText("Şehir başarıyla eklendi !!");
    }//GEN-LAST:event_txtAddToTableActionPerformed

